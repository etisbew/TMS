<?php

namespace TMS\UserJoinPageBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * UserType
 */
class UserType
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var \Doctrine\Common\Collections\Collection
     */
    private $user;

    /**
     * Constructor
     */
    public function __construct()
    {
        $this->user = new \Doctrine\Common\Collections\ArrayCollection();
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return UserType
     */
    public function setName($name)
    {
        $this->name = $name;
    
        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Add user
     *
     * @param \TMS\UserJoinPageBundle\Entity\User $user
     * @return UserType
     */
    public function addUser(\TMS\UserJoinPageBundle\Entity\User $user)
    {
        $this->user[] = $user;
    
        return $this;
    }

    /**
     * Remove user
     *
     * @param \TMS\UserJoinPageBundle\Entity\User $user
     */
    public function removeUser(\TMS\UserJoinPageBundle\Entity\User $user)
    {
        $this->user->removeElement($user);
    }

    /**
     * Get user
     *
     * @return \Doctrine\Common\Collections\Collection 
     */
    public function getUser()
    {
        return $this->user;
    }
		public function __toString()
	{
		return $this->getName() ? $this->getName() : "";
	}
}
